    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            String idPhim = txtFilmID.getText().trim();
            String tenPhim = txtFilmName.getText().trim();
            String tacGia = txtFilmTG.getText().trim();
            String dienVien = txtFilmActor.getText().trim();
            String thoiLuong = txtFilmTL.getText().trim();
            String ngonNgu = txtFilmLang.getText().trim();
            String moTa = txtFilmDes.getText().trim();
            String anhPhim = txtFilmAnh.getText().trim();
            
            // Lấy danh sách thể loại từ checkbox (theo tên)
            List<String> theLoaiList = new ArrayList<>();
            if (ckbCoTrang.isSelected()) theLoaiList.add("Cổ trang - Thần thoại");
            if (ckbFunny.isSelected())    theLoaiList.add("Hài hước");
            if (ckbHorror.isSelected())   theLoaiList.add("Kinh dị - Ma");
            if (ckbFamily.isSelected())   theLoaiList.add("Gia đình - Học đường");
            if (ckbHoatHinh.isSelected()) theLoaiList.add("Hoạt hình");
            if (ckbTamLy.isSelected())    theLoaiList.add("Tâm lý - Tình cảm");
            
            // Kiểm tra dữ liệu đầu vào
            
            if (idPhim.isEmpty() || tenPhim.isEmpty() || tacGia.isEmpty() || dienVien.isEmpty() ||
                    thoiLuong.isEmpty() || ngonNgu.isEmpty() || moTa.isEmpty() || theLoaiList.isEmpty()) {
                
                JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!tacGia.matches("[\\p{L}\\s,]+")) {
            JOptionPane.showMessageDialog(this, "Tên tác giả không được chứa kí tự đặc biệt hoặc số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!ngonNgu.matches("[\\p{L}\\s,]+")){
            JOptionPane.showMessageDialog(this, "Ngôn ngữ không hợp lệ!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!dienVien.matches("[\\p{L}\\s,]+")) {
            JOptionPane.showMessageDialog(this, "Tên diễn viên không được chứa số hoặc ký tự đặc biệt!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
            // Tạo đối tượng Phim
            Phim phim = new Phim(idPhim, tenPhim, tacGia, theLoaiList, thoiLuong, ngonNgu, dienVien, moTa, anhPhim);
            
            // Thêm phim
            if (PhimDAO.themPhim(phim)) {
                model.addRow(new Object[]{idPhim, tenPhim, tacGia, String.join(", ", theLoaiList), thoiLuong, ngonNgu, dienVien, moTa, anhPhim});
                JOptionPane.showMessageDialog(this, "Thêm phim thành công!");
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm phim thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(AddFilm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

